    private void AtualizaCombo() {
        Connection con = Conexao.AbrirConexao();
        PacienteDAO sql = new PacienteDAO(con);
        List<Paciente> lista = new ArrayList<>();
        lista = sql.ListarComboPaciente();
        Combopa.addItem("");
        
        for (Paciente b : lista) {
            Combopa.addItem(b.getNome());
        }
        
        Conexao.FecharConexao(con);
    }
    private void InserirDados(int cod) {
        Connection con = Conexao.AbrirConexao();
        ConsultaDAO sql = new ConsultaDAO(con);
        List<Consulta> lista = new ArrayList<>();
        lista = sql.CapturarConsulta(cod);
        
        for (Consulta a : lista) {
            Combopa.setSelectedItem(a.getNome());
            jTF_DataCad.setText(a.getDatacad());
            jTF_DataCons.setText(a.getDatacons());
            jTF_Idp.setText(""+a.getIdp());
            ComboMed.setSelectedItem(a.getMedico());
        }
        
        Conexao.FecharConexao(con);
    }

