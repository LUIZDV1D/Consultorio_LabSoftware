    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed

        String c = jTF_Cod1.getText();

        Connection con = Conexao.AbrirConexao();
        ConsultaDAO sql = new ConsultaDAO(con);

        int cod = Integer.parseInt(c);
        if (sql.testar_Consulta(cod) == false) {
            JOptionPane.showMessageDialog(null, "Código não encontrado no Banco",
                "Centro Medico", JOptionPane.ERROR_MESSAGE);
        }

        if (c.equals("")) {
            JOptionPane.showMessageDialog(null, "Digite um Código para Atualizar",
                "Centro Medico", JOptionPane.WARNING_MESSAGE);
        }


        InserirDados(cod);
        jTF_Cod1.setText("");
    }//GEN-LAST:event_btnOkActionPerformed

    private void CombopaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CombopaActionPerformed
        Connection con = Conexao.AbrirConexao();
        PacienteDAO sql = new PacienteDAO(con);
        List<Paciente> lista = new ArrayList<>();
        String nome = Combopa.getSelectedItem().toString();

        lista = sql.ConsultarCodigoPaciente(nome);

        for (Paciente b : lista) {
            int a = b.getId();
            jTF_Idp.setText("" + a);
        }

        Conexao.FecharConexao(con);
    }//GEN-LAST:event_CombopaActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        String idp = jTF_Idp.getText();
        String paciente = Combopa.getSelectedItem().toString();
        String datacad = jTF_DataCad.getText();
        String datacons = jTF_DataCons.getText();
        String consulta = ComboMed.getSelectedItem().toString();

        if (paciente.equals("")) {
            JOptionPane.showMessageDialog(null, "Nenhum campo pode estar vazio",
                "Centro Medico", JOptionPane.WARNING_MESSAGE);
        } else {
            Connection con = Conexao.AbrirConexao();
            ConsultaDAO sql = new ConsultaDAO(con);
            int num = Integer.parseInt(idp);
            Consulta a = new Consulta();
            
            a.setIdp(num);
            a.setNome(paciente);
            a.setDatacad(datacad);
            a.setDatacons(datacons);
            a.setMedico(consulta);
            

            sql.Alterar_Consulta(a);
            Conexao.FecharConexao(con);

            jTF_Idp.setText(idp);
            jTF_DataCad.setText("");
            jTF_DataCons.setText("");


            JOptionPane.showMessageDialog(null, "Informações atualizadas",
                "Centro Médico", JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_btnAlterarActionPerformed

